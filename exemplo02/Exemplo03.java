package exemplo02;

public class Exemplo03 {
	
	public static void main(String[] args) {
		
		//caracter especial dentro da string 
		
		String text = "We are the so-called \"Vikings\" from the north";
		System.out.println(text);
		
		//caracter unico 
		
		char text02 = 'Y';
		System.out.println(text02);
		
		// quebrar linha = \n
		
		String text03 = "We are also \ncalled Vikings from the north";
		System.out.println(text03);
	}
}
