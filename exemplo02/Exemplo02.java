package exemplo02;

public class Exemplo02 {
	public static void main(String[] args) {

		//soma de valores int
		
		int numero01 = 10;
		int numero02 = 20;

		int soma = numero01 + numero02;
		System.out.println("soma01: "+soma);
		
		//soma de caracteres
		
		String numero03 = "10";
		String numero04 = "10";
		String soma01 = numero03+numero04;
		System.out.println("soma02: "+soma01);
		
		// soma de caracter com int
		
		String soma02 = numero01+numero03;
		System.out.println("soma03: "+soma02);
		
		
	}
}
